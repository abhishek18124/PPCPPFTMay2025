#include<iostream>

using namespace std;

int main() {

	int x; // variable declaration

	x = 10; // variable assignment

	cout << x << endl;

	int y;

	y = 10 + 20;

	cout << y << endl;

	int z;

	z = 3.14; // implicit typecasting // .14 is truncated and 3 is stored

	cout << z << endl;

	return 0;
}